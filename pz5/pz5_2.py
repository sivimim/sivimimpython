#Описать функцию Minmax(X, Y), записывающую в переменную Х минимальное из значений Х и Y, а в переменную Y — максимальное из этих значений (Х и Y вещественные параметры, являющиеся одновременно входными и выходными).
#Используя четырс вызова этой функции, найти минимальное и максимальное из данных чисел А, В, С, D.

def Minmax(X, Y):
    if X > Y:
        X, Y = Y, X  # Меняем местами, если X больше Y
    return X, Y  # Возвращаем минимальное и максимальное значения

# Входные данные
A = float(input("Введите число A: "))
B = float(input("Введите число B: "))
C = float(input("Введите число C: "))
D = float(input("Введите число D: "))

# Находим минимальное и максимальное значения
min_ab, max_ab = Minmax(A, B)
min_cd, max_cd = Minmax(C, D)

# Теперь находим окончательные минимальные и максимальные значения
min_f, max_f = Minmax(min_ab, min_cd)
max_f = max(max_ab, max_cd)

print(f"Минимальное значение: {min_f}")
print(f"Максимальное значение: {max_f}")
